package Books;

import Author.Author;
import Genre.Genre;

/**
 *
 * @author Ma2017
 */
public class Book {
    String name;
    Author author;
    Genre genre;
    
    public Book(){
        
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Author getAuthor() {
        return author;
    }

    public void setAuthor(Author author) {
        this.author = author;
    }

    public Genre getGenre() {
        return genre;
    }

    public void setGenre(Genre genre) {
        this.genre = genre;
    }

    
    
}
